/**
 * 
 * @param areaCode 目前选择的行政区域编码
 * @param disasterAreaCode 灾害的行政区域编码
 */
export function isAreaCodeContain(areaCode: number | undefined, disasterAreaCode: number) {
  if (!areaCode || areaCode === ALL_NATION.code) return true
  let diff = 1 // 默认县级，diff===0,<1
  if (areaCode % 10000 === 0) diff = 10000 // 省级，diff<10000
  else if (areaCode % 100 === 0) diff = 100 //市级, diff<100
  return Math.floor(areaCode / diff) === Math.floor(disasterAreaCode / diff)
}

