/**四舍五入保留合适的位数,返回的number类型 */
export function numberFixedNum<T extends number | undefined>(num: T, precise = 0): T {
  if (num === undefined) return num
  if (precise === 0) return Math.round(num) as unknown as T
  const multi = Math.pow(10, precise)
  return Math.round((num as number) * multi) / multi as unknown as T
}

/**四舍五入保留合适的位数 */
export function numberFixed<T extends number | undefined>(num: T, precise = 0) {
  return numberFixedNum(num, precise)?.toFixed(precise) as T extends undefined ? undefined : string
}

