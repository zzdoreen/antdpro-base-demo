export type ObjectWithNumberValue<T = {}> = T &
{ total: number } & {
  [key: string]: number
  [key: number]: number
}

/**
 * 返回数据格式化为图表数据格式
 * @param data 源数据
 * @param options 配置项： key-主键 colKey-列键 count-计数的字段、默认'count' nameList-主键对应的字典表
 */
export function formatTableData<T>(data: T[], options: {
  key: keyof T,
  colKey?: keyof T,
  count?: string,
  nameList?: { label: string, value: number }[],
}) {
  const { key, colKey, count = 'count', nameList } = options
  let formattedData = data.reduce((pre: ObjectWithNumberValue<T>[], cur) => {
    const newKey = cur[key] as unknown as number
    const name = nameList ? getNameByListId(nameList, newKey) : newKey
    let obj: ObjectWithNumberValue<T> | undefined = pre.find(e => (e.name as any as string) === name)
    if (obj) {
      // @ts-ignore
      obj.total += cur[count] ?? 1
    }
    else {
      obj = {
        total: cur[count] ?? 1, // 未指明count，则设为1
        name,
        [key]: cur[key]
      } as unknown as ObjectWithNumberValue<T>
      pre.push(obj)
    }
    if (colKey) {
      obj[cur[colKey] as unknown as number] = cur[count]
    }
    return pre
  }, [])
  if (nameList) {
    formattedData = formattedData.map(e => ({ ...e, order: nameList.findIndex(({ value }) => value === e[key]) }))
  }
  return formattedData.sort((a, b) => a.order - b.order)
}

