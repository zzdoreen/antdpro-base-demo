/** 获取缩写的震中位置 */
export function getShortEpicenter(province: string, city: string, county: string) {
  if ((!province || !city) && !county) return ''
  const fullname = ['香港', '澳门'].includes(province) ?
    province + '特别行政区' + county :
    // 直辖市：province === city
    province === city ? province + county : province + city + county
  let i = shortnames.findIndex(e => e[0] === fullname)
  if (i > -1) {
    // 列表里存在的，直接返回
    return shortnames[i][1]
  }
  // 不在列表里的,尝试处理
  const p = province.slice(0, ['黑龙江', '内蒙古'].some((s) => province.startsWith(s)) ? 3 : 2);
  let c = county;
  // 理县、郊区，这种长度为2的不做处理
  if (c.length > 2) {
    // 处理少数民族自治区县旗
    if (c.indexOf('族') >= 0)
      nations.forEach((n) => {
        const index = county.indexOf(n);
        if (index >= 2) {
          c = c.slice(0, index);
        } else if (index >= 0) {
          // 东乡族自治县
          c = n;
        }
      });
    else if (c.endsWith('矿区') || c.endsWith('林区')) {
      c = c.slice(0, c.length - 2);
    }
    // 处理浦东新区、滨海新区等
    else if (c.indexOf('新区') > -1 && c.length >= 4) {
      c = c
    }
    // 处理一般结尾的县、市、区,但不处理 旗
    else if (c.indexOf('旗') === -1) {
      c = c.slice(0, c.length - 1);
    }
  }
  return p + c;
}

