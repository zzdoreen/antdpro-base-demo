const maxTimeout = 2 ** 31 - 1
/**
 * 超长定时器，>24d
 * @returns 新的定时器
 */
export function getLongTimeOutFunc(func: Function, t: number) {
  let timer = 0 as number
  function setLong(func: Function, t: number) {
    const left = t - maxTimeout
    if (left > 0) {
      timer = setTimeout(() => {
        timer = setLong(func, left)
      }, maxTimeout) as unknown as number;
    } else
      timer = setTimeout(func, t);
    return timer
  }
  setLong(func, t)
  return () => clearTimeout(timer)
}


/**
 * 格式化unix时间值
 * @param value 
 */
export const UnixTimeRender = (value: number, inValidText = '') => {
  if (!value) return '-'
  const m = moment.unix(value)
  if (m.isValid())
    return m.format('YYYY-MM-DD HH:mm:ss')
  return inValidText
}

